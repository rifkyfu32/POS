    private void txtCariKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtCariKeyReleased
        // TODO add your handling code here:        
        for (int i = 0; i < tblPembelian.getRowCount(); i++) {
            if (txtCari.getText().trim().equals("")) {
                tblPembelian.clearSelection();
            } else if (tblPembelian.getValueAt(i, 0).toString().toLowerCase().startsWith(txtCari.getText().replaceFirst(txtCari.getText(), txtCari.getText().toLowerCase()))) {
                //select baris yang ditemukan
                tblPembelian.getSelectionModel().setSelectionInterval(i, i);
                //scroll ke baris tersebut kalau ada di bawah atau bagian atas
                ComponentUtils.scrollToRect(tblPembelian, i);
                break;
            }
        }
    }//GEN-LAST:event_txtCariKeyReleased


