    private void refreshTable() {
        pembelianList = Main.getTransaksiServices().getPembelians();
        tblPembelian.setModel(new TransaksiTableModel(pembelianList));
    }

    private class TransaksiTableModel extends AbstractTableModel {

        private final List<Pembelian> pembelians;

        public TransaksiTableModel(List<Pembelian> pembelians) {
            this.pembelians = pembelians;
        }

        @Override
        public int getRowCount() {
            return pembelians.size();
        }

        @Override
        public int getColumnCount() {
            return 5;
        }

        @Override
        public Object getValueAt(int i, int i1) {
            Pembelian p = pembelians.get(i);
            switch (i1) {
                case 0:
                    return p.getNota();
                case 1:
                    return p.getTanggal();
                case 2:
                    return p.getTotal();
                case 3:
                    return p.getStatus();
                case 4:
                    return p.getKeterangan();
                default:
                    return null;
            }
        }

        @Override
        public Class<?> getColumnClass(int columnIndex) {
            if (columnIndex == 2) {
                return BigDecimal.class;
            } else if (columnIndex == 3) {
                return Integer.class;
            }
            return String.class;
        }

        @Override
        public boolean isCellEditable(int rowIndex, int columnIndex) {
            return columnIndex == 3;
        }

        @Override
        public void setValueAt(Object aValue, int rowIndex, int columnIndex) {
            Pembelian p = pembelians.get(rowIndex);
            if (columnIndex == 3) {
                p.setStatus((Integer) aValue);
                Main.getTransaksiServices().simpan(p);
            }
        }

    }

