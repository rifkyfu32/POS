    private void txtSearchKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtSearchKeyReleased
       for (int i = 0; i < tblSales.getRowCount(); i++) {
            if (txtSearch.getText().trim().equals("")) {
                tblSales.clearSelection();
            } else if (tblSales.getValueAt(i, 0).toString().toLowerCase().startsWith(txtSearch.getText().replaceFirst(txtSearch.getText(), txtSearch.getText().toLowerCase()))) {
                //select baris yang ditemukan
                tblSales.getSelectionModel().setSelectionInterval(i, i);
                //scroll ke baris tersebut kalau ada di bawah atau bagian atas
                ComponentUtils.scrollToRect(tblSales, i);
                break;
            }
        }
}//GEN-LAST:event_txtSearchKeyReleased

