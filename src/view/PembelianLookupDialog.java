    private class SalesSelectionListener implements ListSelectionListener {

        @Override
        public void valueChanged(ListSelectionEvent e) {
            if (tblSales.getSelectedRow() >= 0) {
                pembelian = pembelianList.get(tblSales.getSelectedRow());
            }
        }

    }

    private class SalesTableModel extends AbstractTableModel {

        private final List<Pembelian> pembelians;

        public SalesTableModel(List<Pembelian> pembelians) {
            this.pembelians = pembelians;
        }

        @Override
        public int getRowCount() {
            return pembelians.size();
        }

        @Override
        public int getColumnCount() {
            return 5;
        }

        @Override
        public Object getValueAt(int rowIndex, int columnIndex) {
            Pembelian p = pembelians.get(rowIndex);
            switch (columnIndex) {
                case 0:
                    return p.getNota();
                case 1:
                    return p.getTanggal();
                case 2:
                    return p.getTotal();
                case 3:
                    return p.getStatus();
                case 4:
                    return p.getKeterangan();
                default:
                    return "";
            }
        }

    }

