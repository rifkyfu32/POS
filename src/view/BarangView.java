    private void clearForm() {
        barang = null;
        tblSalesDetail.setModel(new SalesDetailTableModel(barangs));
        txtNama.setText("");
        txtBeli.setValue(null);
        txtGrosir.setValue(null);
        txtEceran.setValue(null);
        txtSatuan.setText("");
        txtStock.setText("");
    }

    private void enableForm(boolean status) {
        txtNama.setEnabled(status);
        txtBeli.setEnabled(status);
        txtGrosir.setEnabled(status);
        txtEceran.setEnabled(status);
        txtSatuan.setEnabled(status);
        txtStock.setEnabled(status);
    }

    private boolean validateForm() {
        if (txtNama.getText().trim().equals("")) {
            JOptionPane.showMessageDialog(this, "Nama Barang tidak boleh kosong!", "Error", JOptionPane.ERROR_MESSAGE);
            txtNama.requestFocusInWindow();
            return false;
        } else if (txtBeli.getText().trim().length() == 0) {
            JOptionPane.showMessageDialog(this, "Harga Beli tidak boleh kosong!", "Error", JOptionPane.ERROR_MESSAGE);
            txtBeli.requestFocusInWindow();
            return false;
        } else if (txtGrosir.getText().trim().length() == 0) {
            JOptionPane.showMessageDialog(this, "Harga Grosir tidak boleh kosong!", "Error", JOptionPane.ERROR_MESSAGE);
            txtGrosir.requestFocusInWindow();
            return false;
        } else if (txtEceran.getText().trim().length() == 0) {
            JOptionPane.showMessageDialog(this, "Harga Eceran tidak boleh kosong!", "Error", JOptionPane.ERROR_MESSAGE);
            txtEceran.requestFocusInWindow();
            return false;
        } else if (txtSatuan.getText().trim().equals("")) {
            JOptionPane.showMessageDialog(this, "Satuan tidak boleh kosong!", "Error", JOptionPane.ERROR_MESSAGE);
            txtSatuan.requestFocusInWindow();
            return false;
        } else if (txtStock.getText().trim().equals("")) {
            JOptionPane.showMessageDialog(this, "Stock tidak boleh kosong!", "Error", JOptionPane.ERROR_MESSAGE);
            txtStock.requestFocusInWindow();
            return false;
        }
        return true;
    }

    private void loadFormToModel() {
        barang.setNama(txtNama.getText());
        BigDecimal beli = new BigDecimal(new Double(txtBeli.getValue().toString()));
        BigDecimal eceran = new BigDecimal(new Double(txtEceran.getValue().toString()));
        BigDecimal grosir = new BigDecimal(new Double(txtGrosir.getValue().toString()));
        barang.setHargaBeli(beli);
        barang.setHargaJualEceran(eceran);
        barang.setHargaJualGrosir(grosir);
        barang.setSatuan(txtSatuan.getText());
        barang.setStock(Integer.parseInt(txtStock.getText()));
    }

    private void loadModelToForm() {
        txtNama.setText(barang.getNama());
        txtSatuan.setText(barang.getSatuan());
        txtBeli.setValue(barang.getHargaBeli());
        txtGrosir.setValue(barang.getHargaJualGrosir());
        txtEceran.setValue(barang.getHargaJualEceran());
        txtStock.setText(String.valueOf(barang.getStock()));
    }

    private void refreshTable() {
        barangs = Main.getMasterServices().getBarangs();
        tblSalesDetail.setModel(new SalesDetailTableModel(barangs));
    }

