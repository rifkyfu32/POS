    private class SalesDetailTableModel extends AbstractTableModel {

        private final List<Barang> listBarangs;

        public SalesDetailTableModel(List<Barang> listBarangs) {
            this.listBarangs = listBarangs;
        }

        @Override
        public int getRowCount() {
            return listBarangs.size();
        }

        @Override
        public int getColumnCount() {
            return 7;
        }

        @Override
        public Object getValueAt(int rowIndex, int columnIndex) {
            Barang b = listBarangs.get(rowIndex);
            switch (columnIndex) {
                case 0:
                    return b.getKode();
                case 1:
                    return b.getNama();
                case 2:
                    return b.getHargaBeli();
                case 3:
                    return b.getHargaJualGrosir();
                case 4:
                    return b.getHargaJualEceran();
                case 5:
                    return b.getSatuan();
                case 6:
                    return b.getStock();
                default:
                    return "";
            }
        }

        @Override
        public Class<?> getColumnClass(int columnIndex) {
            if (columnIndex > 1 && columnIndex < 5) {
                return BigDecimal.class;
            } else if (columnIndex == 6) {
                return Integer.class;
            }
            return String.class;
        }

    }

    private class BarangTableSelection implements ListSelectionListener {

        @Override
        public void valueChanged(ListSelectionEvent lse) {
            if (tblSalesDetail.getSelectedRow() >= 0) {
                barang = barangs.get(tblSalesDetail.getSelectedRow());
                barang = Main.getMasterServices().getBarang(barang.getKode());
                loadModelToForm();                
                enableForm(false);
                txtCari.setEnabled(true);
                btnDelete.setEnabled(true);
                btnAdd.setEnabled(false);
                btnCancel.setEnabled(true);
                btnEdit.setEnabled(true);
                btnSave.setEnabled(false);
            }
        }

    }

